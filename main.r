signal <- function(N=100, frequency=1, amplitude=1) {
  x <- seq(0,1,by=1/N)

  ans <- amplitude * sin(frequency * pi * x)
  
  return(ans)
}
noise <- function(x, multiplier=0.5) {
  amp <- max(x) - min(x)
  ans <- x + rnorm(length(x), sd=multiplier*amp)
  
  return(ans)
}

averagesMethod <- function(x){
  pos <- x > 0
  neg <- x < 0

  sumPos <- sum(pos)
  sumNeg <- sum(neg)

  avPos <- sumPos / length(pos)
  avNeg <- sumNeg / length(neg)

  if (avPos > avNeg) {
    return(+1)
  } else {
    return(-1)
  }
}

detect <- function(y) {
  pos <- sum(y > 0)
  neg <- sum(y < 0)
  
  if (pos > neg) {
    return(+1)
  } else {
    return(-1)
  }
}

